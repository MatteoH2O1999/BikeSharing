package BikeSharing.Usertypes.Proof;

/**
 * Proof that the user is eligible to be considered a student
 */

public class StudentProof {

    /**
     * Lazy proof
     */

    public boolean proof;

    /**
     * Lazy constructor
     * @param b boolean
     */

    public StudentProof(boolean b) {
        this.proof = b;
    }
    
}
